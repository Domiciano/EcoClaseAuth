package edu.co.icesi.ecos16;

public class User {

    public String id;
    public String name;
    public String city;
    public String email;

    public User(){}

    public User(String id, String name, String city, String email) {
        this.id = id;
        this.name = name;
        this.city = city;
        this.email = email;
    }
}
